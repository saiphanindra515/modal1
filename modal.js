$(document).ready(function(){

$(".overlay").hide();
$(".modalDesign").hide();

$(".modaltrigger").click(function(){

$(".overlay").show();
$(".modalDesign").show();

});

$("#close").click(function(){

$(".overlay").hide();
$(".modalDesign").hide();

});
$(".overlay").click(function(){

	$(".overlay").hide();
	$(".modalDesign").hide();
})



	




});